  { label: '官方客服', path: '/' },
  { label: '设置', path: '/' }
]

const store = useUserStore()
const router = useRouter()
const logout = async () => {
  // Dialog.confirm的返回值是一个promise，可以不用写then和catch，直接加一个await来控制是否往下执行
  await Dialog.confirm({
    title: '温馨提示',
    message: '您确认要退出优医问诊吗？',
    cancelButtonText: '取消',
    confirmButtonText: '确认'
  })
  store.delUser()
  router.push('/login')
}
</script>

